@Composable
fun BasicFeedList(posts: List<Post>) {
    LazyColumn(
        modifier = Modifier.fillMaxSize(),
        contentPadding = PaddingValues(vertical = 8.dp),
        verticalArrangement = Arrangement.spacedBy(8.dp)
    ) {
        // Itera sobre la lista de posts
        items(
            items = posts,
            key = { post -> post.id } // clave única por id
        ) { post ->
            PostCard(post = post) // tu componente PostCard
        }
    }
}

@Composable
fun FeedWithIndex(posts: List<Post>) {
    LazyColumn {
        itemsIndexed(posts) { index, post ->
            Text("Post #$index: ${post.username}")
        }
    }
}
